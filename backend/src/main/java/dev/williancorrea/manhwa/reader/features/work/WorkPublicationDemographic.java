package dev.williancorrea.manhwa.reader.features.work;

public enum WorkPublicationDemographic {
  SHOUNEN, // Obras (geralmente mangás e animes) voltadas ao público jovem masculino, com foco em ação, aventura, amizade e superação.
  SEINEN,  // Produções direcionadas a homens adultos, costumam ter temas mais complexos, violência realista, política ou dilemas psicológicos.
  JOSEI,   // Histórias pensadas para mulheres adultas, explorando relacionamentos, vida profissional e emoções de forma mais madura.
  SHOUJO,  // Narrativas voltadas a meninas adolescentes, frequentemente centradas em romance, sentimentos e crescimento pessoal.
  YAOI,    // Subgênero que retrata relações românticas ou sexuais entre personagens masculinos, geralmente criado para o público feminino.
  YURI,    // Subgênero focado em relacionamentos amorosos entre personagens femininas, variando do romance leve ao conteúdo mais explícito.
  HENTAI,  // Categoria de anime ou mangá com conteúdo pornográfico explícito.
  COMIC,   // Termo amplo para histórias contadas em quadrinhos, incluindo produções ocidentais como as da Marvel e DC.
  UNKNOWN  // Indica que o gênero ou a classificação da obra não foi identificada ou não se encaixa nas categorias conhecidas.
}
